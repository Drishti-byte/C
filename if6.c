/*program to check if a person is retired or not*/
#include<stdio.h>
int main()
{
    int age,gen,male,female;
    printf("Enter gender and age:");
    scanf("%d %d",&gen,&age);
    if(male && age >= 60)
    printf("\nYou are retired\n");
    if(female && age >= 56)
    printf("\nYou are retired\n");
    return 0;
}